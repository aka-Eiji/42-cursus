#include "libft.h"

void	ft_putnbr(int n)
{
	long int	nb;

	nb = n;
	if (n < 0)
	{
		nb = -(long int)n;
		ft_putchar('-');
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putchar((nb % 10) + '0');
	}
	else
		ft_putchar(nb + '0');
}
