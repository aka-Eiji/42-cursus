#include "libft.h"

void	ft_putarr(int *d, int n)
{
	int	i;

	i = 0;
	while (i < n)
	{
		ft_putnbr(d[i++]);
		if (n != 0)
			ft_putchar(' ');
	}
	ft_putchar('\n');
}
